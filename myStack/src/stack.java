
public interface stack {

	
	public void Push(String str); // Method to push some data in stack
	public String Pop();  //Method to take out data from top of the stack
	public String Top();  //Method to get the top element of the stack
	public boolean Isempty();  //Method to check whether the stack is empty or not
	public boolean Isfull();  //Method to check whether the stack is full or not
	
}
